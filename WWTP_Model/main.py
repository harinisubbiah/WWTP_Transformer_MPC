class WWTPTransformer(nn.Module):
    def __init__(self, input_dim, d_model=64,
                 nhead=4, layers=3,
                 horizon=10, n_states=9):

        super().__init__()

        self.horizon = horizon
        self.n_states = n_states

        self.embedding = nn.Linear(input_dim, d_model)

        encoder_layer = nn.TransformerEncoderLayer(
            d_model=d_model,
            nhead=nhead,
            batch_first=True
        )

        self.encoder = nn.TransformerEncoder(
            encoder_layer, layers
        )

        self.fc = nn.Linear(d_model,
                            horizon*n_states)

    def forward(self, x):
        x = self.embedding(x)
        x = self.encoder(x)
        x = x[:, -1, :]
        x = self.fc(x)
        return x.view(-1,
                      self.horizon,
                      self.n_states)

model = WWTPTransformer(
    input_dim=X_all.shape[2],
    horizon=Y_all.shape[1],
    n_states=Y_all.shape[2]
)

