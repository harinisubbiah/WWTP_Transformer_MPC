def mpc_cost(u, model, x0, device):

    x = x0.clone().to(device)

    total_cost = 0.0

    for t in range(len(u)):

        # apply control
        x[:, -1, CONTROL_INDEX] = float(u[t])

        with torch.no_grad():
            pred = model(x)

        nh4 = pred[:, :, NH4_INDEX]
        ss  = pred[:, :, SS_INDEX]

        COD = 1.42 * ss

        total_cost += (
    5.0 * torch.mean(nh4**2)        # strong NH4 control
    + 1.0 * torch.mean(COD**2)      # moderate COD
    + 0.05 * (u[t]**2)              # 🔥 REDUCE control penalty
)
        # get next state
        next_state = pred[:, 0, :]   # (1, 9)

        # shift window
        x = torch.roll(x, shifts=-1, dims=1)

        # ✅ FIX: update only state part
        x[:, -1, :9] = next_state

        # re-apply control
        x[:, -1, CONTROL_INDEX] = float(u[t])

    return total_cost.item()

